Config.DatabaseLoadAllVehicleData = function()
	return MySQL.query.await('SELECT `owner`, `plate` FROM `owned_vehicles`')
end

Config.DatabaseGetVehiclesPlateByOwner = function(identifier)
	return MySQL.query.await('SELECT `plate` FROM `owned_vehicles` WHERE `owner` = ?', { identifier })
end

Config.DatabaseGetVehicleOwnerByPlate = function(plate)
	return MySQL.prepare.await('SELECT `owner` FROM `owned_vehicles` WHERE `plate` = ?', { plate })
end